package org.mozilla.browserquest.staticdata.template;

public class DoorTemplate {

    private int x;
    private int y;
    private int p;
    private int tcx;
    private int tcy;
    private String to;
    private int tx;
    private int ty;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getP() {
        return p;
    }

    public void setP(int p) {
        this.p = p;
    }

    public int getTcx() {
        return tcx;
    }

    public void setTcx(int tcx) {
        this.tcx = tcx;
    }

    public int getTcy() {
        return tcy;
    }

    public void setTcy(int tcy) {
        this.tcy = tcy;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }

    public int getTx() {
        return tx;
    }

    public void setTx(int tx) {
        this.tx = tx;
    }

    public int getTy() {
        return ty;
    }

    public void setTy(int ty) {
        this.ty = ty;
    }
}

